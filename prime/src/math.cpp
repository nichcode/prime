
#include "prime/math.h"

#include <cmath>

namespace prime {

    f32 
    math::sqrt(f32 num)
    {
        return sqrtf(num);
    }

    f32 
    math::tan(f32 num)
    {
        return tanf(num);
    }

    f32 
    math::cos(f32 num)
    {
        return cosf(num);
    }

    f32 
    math::sin(f32 num)
    {
        return sinf(num);
    }

} // namespace prime
