
#include "prime/maths/maths.h"
#include <math.h>

namespace prime::maths {

    f32 sqrt(f32 num)
    {
        return sqrtf(num);
    }

    f32 tan(f32 num)
    {
        return tanf(num);
    }

    f32 cos(f32 num)
    {
        return cosf(num);
    }

    f32 sin(f32 num)
    {
        return sinf(num);
    }
    
} // namespace prime::maths
